            <p class="mx-auto text-justify">MEEGA+ é um método para a avaliação da qualidade de 
            jogos usados para o ensino de computação/engenharia de software. O 
            método MEEGA+ foi sistematicamente desenvolvido com base nos resultados da
            análise do estado da arte e da prática e nos resultados da avaliação em
             larga escala da versão inicial do modelo MEEGA, identificado como a 
             abordagem de avaliação
            proeminente e amplamente utilizada na
            prática. O método MEEGA+ é composto por
            um modelo de avaliação (Modelo MEEGA+),
            que sistematicamente define fatores de
            qualidade para avaliar jogos por meio de um instrumento de medição padronizado e
            fornece uma escala, que classifica o jogo avaliado em níveis de qualidade. Além disso o
            método MEEGA+ também define um processo (Processo MEEGA+), que descreve em
            detalhes as fases, atividades e produtos de trabalho para orientar instrutores e
            pesquisadores na condução de avaliações de jogos para o ensino de computação. Uma
            série de estudos de caso foi conduzida de modo a avaliar a confiabilidade e validade do
            instrumento de medição do método MEEGA+. Os resultados da análise da confiabilidade
            indicam uma excelente consistência interna do instrumento de medição do método
            MEEGA+ (Alfa de Cronbach α=.927). Além disso, os resultados da análise da validade,
            obtidos por meio de uma análise fatorial exploratória, confirmam a estrutura original do
            método MEEGA+. Adicionalmente, resultados de uma avaliação do método MEEGA+, sob
            a perspectiva de especialistas, fornecem uma primeira indicação de que o método
            MEEGA+ também é correto, completo, autêntico, consistente e não ambíguo.</p>
            
